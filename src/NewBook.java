    private void autoGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_autoGenerateActionPerformed
        // TODO add your handling code here:
        errorMsg.setVisible(false);
        bookID.setBackground(new Color(255,255,204));
        bookID.setText("");
        bookID.setEditable(false);

        Connection con=DatabaseConnection.connection();
        String sID=bookID.getText();
        int greaterNumber=0;

        Statement st;
        try
        {
            st = con.createStatement();
            ResultSet rs=st.executeQuery("select id from newbook"+";");
            while(rs.next())
            {
                for(;(greaterNumber <= rs.getInt(1));)
                {
                    greaterNumber=greaterNumber+1;
                }
            }
        }
        catch (SQLException ex) {
            Logger.getLogger(NewStudent.class.getName()).log(Level.SEVERE, null, ex);
        }

        String s=Integer.toString(greaterNumber);
        bookID.setText(s);

    }//GEN-LAST:event_autoGenerateActionPerformed

