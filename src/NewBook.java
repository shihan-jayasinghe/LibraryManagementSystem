    private void newBookSaveButton()
    {
       String bID=bookID.getText();
        String bName=name.getText();
        String bPublisher=publisher.getText();
        String bPrice=price.getText();
        String bPublisherYear=publishedYear.getText();

        String query="insert into newBook(id,name,publisher,price,publishedYear)values(?,?,?,?,?);";
        if(!("".equals(publishedYear.getText())) && !("".equals(price.getText())) && !("".equals(publisher.getText())) && !("".equals(name.getText())) && !("".equals(bookID.getText())))
        {
            try(Connection con=DatabaseConnection.connection())
            {
                PreparedStatement pst=con.prepareStatement(query);
                pst.setString(1,bID);
                pst.setString(2,bName);
                pst.setString(3,bPublisher);
                pst.setString(4,bPrice);
                pst.setString(5,bPublisherYear);
                boolean status=pst.execute();

                if(!status)
                {
                    JOptionPane.showMessageDialog(null,"Book is added!");
                    this.setVisible(false);
                    new NewBook().setVisible(true);
                }
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
        else
        {
          errorMsg.setText("Every field is mandatory!!");
          errorMsg.setVisible(true);
        }
    }
