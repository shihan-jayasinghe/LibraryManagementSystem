    private void bookIDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_bookIDFocusLost
        // TODO add your handling code here:
        Connection con=DatabaseConnection.connection();
        String sID=bookID.getText();
        try 
        {
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select id from newbook where id="+"\""+sID+"\""+";");
            
            if(rs.next())
            {
               if(sID.equals(rs.getString(1)))
               {
                   errorMsg.setVisible(true);
                   errorMsg.setText("The student id is already taken!");
                   bookID.setBackground(new Color(255,51,51));
               }
            }
            
        } 
        catch (SQLException ex) {
            Logger.getLogger(NewStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bookIDFocusLost

