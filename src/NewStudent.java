    private void newStudenttSaveButton()
    {
        String sID=studentID.getText();
        String sName=name.getText();
        String sFathersName=fathersName.getText();
        String sCourseName=(String)courseName.getSelectedItem();
        String sBranchName=(String)branchName.getSelectedItem();
        
      String query="insert into newStudent(studentID,name,fathersName,courseName,branchName)values(?,?,?,?,?);";
      if((studentID.getText().matches("[0-9]+")) && !("".equals(name.getText())) && !("".equals(fathersName.getText())))
     {
        try(Connection con=DatabaseConnection.connection())
        {
          PreparedStatement pst=con.prepareStatement(query); 
          pst.setString(1, sID);
          pst.setString(2,sName);
          pst.setString(3,sFathersName);
          pst.setString(4,sCourseName);
          pst.setString(5,sBranchName);
          boolean status=pst.execute();
          
          if(!status)
          {
            JOptionPane.showMessageDialog(null,sName+" is added!");
            this.setVisible(false);
            new NewStudent().setVisible(true);
          }
        }
        catch(Exception e)
        {
          e.printStackTrace();      
        }
      }
    }
