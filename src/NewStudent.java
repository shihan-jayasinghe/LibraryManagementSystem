    private void isIDexist()
    {
        Connection con=DatabaseConnection.connection();
        String sID=studentID.getText();
        try 
        {
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select studentID from newStudent where studentID="+"\""+sID+"\""+";");
            
            if(rs.next())
            {
               if(sID.equals(rs.getString(1)))
               {
                   errorMsg.setVisible(true);
                   errorMsg.setText("The student id is already taken!");
                   studentID.setBackground(new Color(255,51,51));
               }
            }
            
        } 
        catch (SQLException ex) {
            Logger.getLogger(NewStudent.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }
